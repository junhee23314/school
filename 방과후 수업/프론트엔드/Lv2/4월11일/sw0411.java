package sw0409;

public class sw0411 {

	public static void main(String[] args) {
		int 이진수 = 0b01010010101001;
		System.out.println(이진수);
		
		int 팔진수 = 0770405;
		System.out.println(팔진수);
		
		int 십육진수 = 0xA0FB;
		System.out.println(십육진수);
		
		int 이진수2 = 0b01010010101001;
		int 팔진수2 = 012251;
		int 십육진수2 = 0x14A9;
		System.out.println(이진수2);
		System.out.println(팔진수2);
		System.out.println(십육진수2);
		
		long 롱 = 18181818181L;
		// 롱이라는 변수 long형, 18이라는 숫자는 int형
		float 플로트 = 18.2F;
		// 플로트라는 변수는 float형인데 18.2는 double형

		System.out.println("18\r18");
		System.out.println("18\n18");
		
		
	}

}
